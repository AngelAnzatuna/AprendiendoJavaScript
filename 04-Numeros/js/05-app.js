// post-incremento
let puntaje = 5;
console.log(puntaje++); // Devuelve 5
console.log(puntaje); // Devuelve 6; aplica el incremento

// pre-incremento
let puntaje2 = 5;
console.log(++puntaje2); // Aplica el incremento; Devuelve 6
console.log(puntaje2); // Devuelve 6

// post-decremento
let puntaje3 = 5;
console.log(puntaje3--); // Devuelve 5
console.log(puntaje3); // Devuelve 4; aplica el decremento

// pre-decremento
let puntaje4 = 5;
console.log(--puntaje4); // Aplica el decremento; devuelve 4
console.log(puntaje4); // Devuelve 4

// Aplicar incremento o decremento n veces
// Incremento
let puntaje5 = 5;
console.log(puntaje5 += 3); // Devuelve 8 
//Decremento
let puntaje6 = 5;
console.log(puntaje6 -= 2); // Devuelve 3

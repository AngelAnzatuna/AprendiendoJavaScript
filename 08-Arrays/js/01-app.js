// Forma de crear arreglos
// elemento: valores que se encuentran dentro del arreglo
// indices: posición que tiene el elemento en el arreglo
// los arreglos inician en 0
//  Se puede crear arreglos de un o mas elementos
const numeros = [10, 20, 30];

// Forma no común de crear arreglos
const meses = new Array('Enero', 'Febrero', 'Marzo');

console.log(numeros);
console.log(meses);

// Un arreglo que contiene datos de todo tipo
// Un arreglo puede estar dentro de otro arreglo
const deTodo = ['Hola', 10, true, 'si', null, { nombre: 'Angel', trabajo: 'Desarrollador'}, [1, 2, 3]];

console.log(deTodo);

/* Un arreglo es una variable que va a contener multiples elementos */
const puntaje = 1000;
// Dentro del parentesis va a estar la condición
// Siempre se asigna con un signo de igual
// siempre se compara con 2 o 3 signos de igual 
if(puntaje == 1000) {
    console.log('Si es igual');
} 
// En el else va cuando no se cumple la condición
else {
    console.log('No es igual');
}